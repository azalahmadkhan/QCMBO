function f = DeJongFunction(x)
a=[-32 -16 0 16 32 -32 -16 0 16 32 -32 -16 0 16 32 -32 -16 0 16 32 -32 -16 0 16 32;-32 -16 0 16 32 -32 -16 0 16 32 -32 -16 0 16 32 -32 -16 0 16 32 -32 -16 0 16 32];
f = 1/500 ;
for i =1:25
    f = f + 1/(i +(x(1) - a(1,i)).^6 +(x(2) - a(2,i)).^6 );
end
f = power(f,-1);
end